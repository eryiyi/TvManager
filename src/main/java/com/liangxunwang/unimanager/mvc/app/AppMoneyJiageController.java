package com.liangxunwang.unimanager.mvc.app;

import com.liangxunwang.unimanager.model.AdObj;
import com.liangxunwang.unimanager.model.MoneyJiageObj;
import com.liangxunwang.unimanager.model.tip.DataTip;
import com.liangxunwang.unimanager.query.AdQuery;
import com.liangxunwang.unimanager.service.ListService;
import com.liangxunwang.unimanager.service.ServiceException;
import com.liangxunwang.unimanager.util.ControllerConstants;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.ResponseBody;

import java.util.List;

/**
 * Created by zhl on 2015/2/2.
 */
@Controller
public class AppMoneyJiageController extends ControllerConstants {
    @Autowired
    @Qualifier("moneyJiageObjObjService")
    private ListService moneyJiageObjObjService;

    //列表
    @RequestMapping(value = "/getMoneyJiage", produces = "text/plain;charset=UTF-8")
    @ResponseBody
    public String getMoneyJiage(AdQuery query){
        try {
            List<MoneyJiageObj> list = (List<MoneyJiageObj>) moneyJiageObjObjService.list(query);
            DataTip tip = new DataTip();
            tip.setData(list);
            return toJSONString(tip);
        }catch (ServiceException e){
            return toJSONString(ERROR_1);
        }
    }

}
